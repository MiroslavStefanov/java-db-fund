import java.util.Scanner;

public class RevChar {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        char a, b, c;
        a = scanner.nextLine().charAt(0);
        b = scanner.nextLine().charAt(0);
        c = scanner.nextLine().charAt(0);
        System.out.print(c);
        System.out.print(b);
        System.out.print(a);
    }
}
